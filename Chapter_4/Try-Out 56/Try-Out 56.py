# Step 1: Make a list of pizzas you like
# A list is just a collection of items stored together
pizzas = ["Pepperoni", "Margherita", "BBQ Chicken"]

# Step 2: Use a for loop to go through each pizza in the list
# The loop will run once for each pizza name
for pizza in pizzas:
    # Step 3: Print a sentence about each pizza
    # f-string lets us insert the pizza name directly into the sentence
    print(f"I like {pizza} pizza.")

# Step 4: After the loop finishes, print a final statement
# This line runs only once, outside the loop
print("I really love pizza!")


# Step 1: Make a list of animals that share something in common
# Here, all of these animals can be pets
animals = ["Dog", "Cat", "Rabbit"]

# Step 2: Use a for loop to go through each animal in the list
for animal in animals:
    # Step 3: Print a sentence about each animal
    print(f"A {animal} would make a great pet.")

# Step 4: After the loop finishes, print a final statement
print("Any of these animals would make a great pet!")
